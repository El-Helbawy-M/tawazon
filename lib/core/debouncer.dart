import 'dart:async';

class Debouncer {
  //===============================================
  //=============================================== Variables
  //===============================================
  final Duration delay;
  Timer? _timer;

  //===============================================
  //=============================================== Constructor
  //===============================================
  Debouncer({this.delay = const Duration(milliseconds: 500)});

  //===============================================
  //=============================================== Functions
  //===============================================
  void call(Function callback) {
    _timer?.cancel();
    _timer = Timer(delay, () {
      callback();
    });
  }

  void dispose() {
    _timer?.cancel();
  }
} 